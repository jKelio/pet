### --- Zweikampf defensiv ---

**Spielerisch Grundlagen schaffen, um erfolgreich im Zweikampf zu sein**

Spieler müssen Beweglichkeit auf Schlittschuhen entwickeln, um Gegner in Zweikampfsituationen kontrollieren zu können. Junge Kinder lernen Abwehrpositionen und Schlägerführung durch spielbasierte Ansätze, mit Betonung der „Abwehrseite" und ihrer Bedeutung.

**Agilitätstraining**

Fang-Spiele entwickeln Schnelligkeit, Mobilität und Gleichgewicht auf Schlittschuhen. Vier spezifische Spiele: Versteinern und erlösen, Box fangen, Fangen mit Verbotsszonen, Speed Tag.

**1-gegen-1-Übungen**

Fünf stationsbasierte Übungen vermitteln Abwehrpositionierung gegen einen Gegner: 1-1 Umschalten, 1-1 Zonen verteidigen, 1-1 Abwehrseite auf engem Raum, 2 x 1-1 über Kreuz gegen Reifen spielen, 1-1 Schläger klauen.

**Kleinfeldspiele**

Drei Spielformen vermitteln defensive Grundprinzipien mit gleicher Spielerzahl: Schlägerpositionen, Abstandskontrolle und Verteidigung gegen Puck- und Nicht-Puck-Träger.

---

### --- Zweikampf offensiv ---

**Spielerisch Grundlagen schaffen, um erfolgreich im Zweikampf zu sein**

Für erfolgreiche offensive Zweikämpfe benötigen Spieler Stabilität, Schnelligkeit und Beweglichkeit auf Schlittschuhen. Sie müssen den Puck sicher führen und gegen gegnerischen Druck schützen. Durch Täuschungen erlangen Spieler Vorteile gegenüber Abwehrspielern.

Das Training in der Basisschulung konzentriert sich auf die Verbesserung der Geschicklichkeit auf Schlittschuhen und des Puckumgangs. Spielformen ermöglichen es Kindern, ein Gefühl für erfolgreiche Zweikämpfe zu entwickeln.

**Technische Voraussetzungen**

Der Schläger sollte „ca. bis unterhalb des Kinns" reichen. Die obere Hand führt den Schläger, die untere Hand sitzt etwa eine Unterarmlänge entfernt und kann beweglich gleiten. Der Blick richtet sich auf Puck und Spielfeld.

**Grundtechniken der Puckführung**

Trainiert werden: Scheibenführung vor und seitlich am Körper, Vorhand- und Rückhand-Techniken, Puckführung mit einer Hand sowie Beschleunigung, Verzögerung und schnelle Richtungswechsel.

**Täuschungen und Abschirmen**

Kinder lernen in Spielformen wie 1-1, 2-2 und 3-3 Kleinfeldspiele, wie sie sich durch Antäuschungen Vorteile verschaffen oder den Puck durch Abschirmen behaupten.

