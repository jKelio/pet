### --- Ernährung ---

Im Kindes- und Jugendalter werden die Weichen für die Gesundheit im späteren Leben gestellt. Die Ernährung hat dabei vorrangig das Ziel, bei adäquater Energiezufuhr die altersgemäße Versorgung mit allen benötigten Nährstoffen für Gesundheit, Wachstum, Entwicklung und Leistungsfähigkeit sicherzustellen. Neben dieser zentralen Bedeutung für die Kindergesundheit kann eine gesunde Ernährung von Anfang an auch zur Vorbeugung von Krankheiten im späteren Erwachsenenalter, z. B. Herz-Kreislauf-Krankheiten oder Typ-2-Diabetes beitragen.

Wissenschaftliche Erkenntnisse über eine gesunde Ernährung müssen praxisnah formuliert werden, damit sie die Bevölkerung erreichen. Im Public Health wird deshalb die Entwicklung sogenannter Food Based Dietary Guidelines (FBDG) empfohlen, um die klassischen nährstoffbezogenen Empfehlungen in lebensmittelbasierte Empfehlungen zu übersetzen. Traditionell und kulturell geprägte Ernährungsgewohnheiten und Lebensmittelmuster der Bevölkerung sollen berücksichtigt und Empfehlungen für eine Gesamternährung, nicht nur für einzelne Lebensmittel, entwickelt werden. Beispiel für solche FBDG ist etwa der Ernährungskreis der Deutschen Gesellschaft für Ernährung (DGE, www.dge.de) für Erwachsene.

**Praktische Tipps für die Ernährung bei Training/Spiel:**
- Kurz vor einer Belastung/einem Match: Nach Bedarf eventuell eine reife Banane, fettarmer Müsliriegel, eine helle Semmel mit etwas Marmelade, Trockenobst wie Rosinen etc. essen.
- Während der Belastung: Bei kurzen und lockeren Einheiten reicht Wasser als Getränk aus. Zuckerhaltige Getränke oder Snacks sind nicht notwendig!
- Nach einer Belastung: Je nach Bedarf sind eventuell direkt im Anschluss an die Belastung folgende Beispiele fettarmer Lebensmittel eine gute Wahl, da sie eine Kombination aus Kohlenhydraten und Protein enthalten: Fruchtjoghurt, Schokomilch, fettarme Sandwiches, Milchreis etc.

