### --- Ganzheitliche Persönlichkeitsentwicklung ---

Die Rahmentrainingskonzeption der DEB beschreibt einen inhaltlichen Leitfaden für die Spielerausbildung vom Kindereishockey bis zum Anschlusstraining. Sie formuliert altersgerechte Trainingsinhalte, Spielerprofile und Zielspiele für jede Ausbildungsstufe sowie Betreuungsleitlinien und Traineranforderungen. Der zentrale Grundsatz lautet: „Der Mensch muss immer im Mittelpunkt stehen — ein athletenzentriertes Coachen ist unabdingbar!"

**Trainerrolle**

Trainer des Deutschen Eishockey-Bundes leisten unbezahlte, unschätzbare Arbeit für die Sportentwicklung. Sie fungieren als Fachexperten, Zuhörer, Unterstützer, Wertvermittler und gesellschaftliche Bindeglieder. Sie tragen erhebliche Verantwortung für die Entwicklung junger Menschen und werden oft zu Hilferessourcen über ihre Trainingstätigkeit hinaus.

**Persönlichkeitsentwicklung**

Zentrale Entwicklungsbereiche umfassen:
- Schulische Erfolge als Grundlage sportlicher Leistung
- Lebenslanges Lernen durch Offenheit für Neues
- Selbstverantwortung übernehmen
- Selbstvertrauen durch positives Feedback aufbauen
- Gesunde Lebensweise mit ausreichend Ernährung, Schlaf und Regeneration

**Wertvermittlung**

Eishockey als „gespieltes Leben" vermittelt zentrale Werte:
- Respekt: Grundlage für friedvolles Miteinander verschiedener Kulturen
- Fairness: Engagement statt Regelbruch
- Begeisterung: Antrieb für Leistungsentwicklung
- Teamgeist: Zusammenarbeit, gegenseitige Unterstützung und gemeinsame Ziele

