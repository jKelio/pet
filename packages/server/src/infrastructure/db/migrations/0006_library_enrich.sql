### --- Stocktechnik ---

**Grundlagen schaffen — Technik forcieren**

Die Stocktechnik umfasst Puckführung, Finten, Passen und Schießen als fundamentale Fähigkeiten im Eishockey. Das zentrale Ziel der Basisausbildung besteht darin, dass „Spieler ein Gefühl für Schläger und Puck entwickeln".

**Basisfertigkeiten**

Die Fertigkeiten bauen progressiv aufeinander auf, müssen aber nicht alle in Perfektion beherrscht werden, bevor man zur nächsten Stufe übergegangen wird. Der zeitliche Rahmen hängt von Trainings-Häufigkeit, verfügbarer Eiszeit und Mannschaftshomogenität ab.

**Grundlagen**

Schlägereigenschaften: Altersgerechte Schläger sollten verwendet werden, wobei die ideale Länge bis zur Mundhöhe reicht. Weichere Schläger sind beim Erlernen moderner Techniken vorzuziehen.

Grundposition: Der Schläger wird vor dem Körper gehalten mit freien Ellenbogen. Gelenke (Sprung-, Knie-, Hüfte) sind gebeugt. Aktive Handgelenke ermöglichen sichere Puckkontrolle.

**Puckführung/Finten**

Effektive Puckführung bildet die Grundlage für Täuschungen. Wichtige Finte-Komponenten sind: Timing, Antäuschung, Richtungswechsel, Beschleunigung und anschließende Puckkontrolle.

Basisbewegungen umfassen kurzes und weites Dribbeln in verschiedenen Richtungen. Der DEB empfiehlt spezifische Körpertäuschungen wie Front-Fake, Side-Fake und Gretzky-Move.

**Passen und Schießen**

Beide Techniken folgen ähnlichen Grundprinzipien: gezogene Bewegungen mit Gewichtsverlagerung von hinten nach vorne. Pass- und Schussannahmen erfordern ein Schlägerblatt im 90-Grad-Winkel zum ankommenden Puck.

Trainingsaufbau sollte hohe Wiederholungszahlen durch Stationsarbeit und Kleinfeldspiele ermöglichen. Blaue Pucks werden für jüngere Altersklassen empfohlen.

