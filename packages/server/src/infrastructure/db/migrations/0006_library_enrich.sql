### --- Wettspiel in den Ausbildungsstufen ---

Taktische Vorgaben spielen bis Aufbautraining 1 eine untergeordnete Rolle. Der Fokus liegt hier primär auf technischer Entwicklung und dem Erlernen spieltaktischer Prinzipien.

„Prinzipien legen die Grundlage für alle weiteren taktischen Vorgaben im Wettspiel."

**Basisschulung — Spielprinzipien**

Spieler entwickeln Freude am Spiel, lernen das Spiel, Schlägerpositionen und Teamkommunikation. In den Altersgruppen U7, U9, U11 kommen Konzepte hinzu wie Freiraumnutzung, Puckkontrolle mit erhobenem Kopf, Torschusssuche und defensive Grundpositionierung.

**Grundlagentraining — Spielprinzipien**

Training betont Zonenverantwortlichkeiten (defensiv, offensiv), Backcheck-Ausführung, allgemeine Forecheck-Strategie, Positionierung bei Über- und Unterzahl, Rush-Taktiken und Zwei-gegen-eins-Szenarien.

**Aufbautraining 1 — Spielprinzipien**

Fokus verlagert sich auf numerische Überlegenheit schaffen, Cycle-Spiel in der Offensivzone, strukturierte Forecheck-Formationen und erweiterte Körperkontakttechniken über fünf Progressionsstufen.

Ein umfassendes Playbook (PDF) beschreibt taktische Vorgaben für Aufbautraining 1 und darüber hinaus. „Hockey Coach Vision" bietet kostenlosen Zugang zu animierten Trainingsübungen. Zwei E-Books behandeln taktisch-technische Trainingsmethodik im Eishockey aus wissenschaftlicher Perspektive.

