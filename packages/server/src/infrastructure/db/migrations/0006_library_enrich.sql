### --- Defensive Zone ---

**Spielen und Prinzipien in der defensiven Zone vertiefen**

Pädagogische Aspekte stehen über taktischen Vorgaben; grundlegende Taktiken werden durch Prinzipien vermittelt.

**Zone**

Spieler lernen, ihr „Haus" kennenzulernen, um den Raum vor dem eigenen Tor zu schützen. Sie werden mit den vier Ebenen der eigenen Zone vertraut gemacht und lernen positionsspezifische Laufwege. Verschiebungen im System (beim Puckwechsel der Seite) werden vermittelt, wodurch eine Grundaufstellung entsteht.

**Aufbau**

Bei Puckbesitz des Verteidigers bieten sich die Außenstürmer tief an; der Mittelstürmer unterstützt und besetzt die Mittellaufbahn. Alle drei Bahnen sollten besetzt sein. Aufbauoptionen: V-V-Pässe, schnelle Außenpässe oder der Verteidiger läuft selbst mit dem Puck.

**Gegenangriff (Rush)**

Beim schnellen Umschalten sollten alle drei Bahnen besetzt sein. Mittelbahnzug zum Tor und Kreierung von Torchancen sind essentiell.

**Backcheck**

Bei Puckverlust im gegnerischen Drittel unterstützt der hohe Stürmer die Verteidiger, während die anderen beiden schnell zurücklaufen und bremsen. Der Schulterblick hilft, Gegenspieler zu beobachten.

**Bully**

Spieler lernen die Positionierung bei Bullys in verschiedenen Zonen und die entsprechenden Laufwege nach Gewinn oder Verlust.

