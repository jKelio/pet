### --- Zweikampf defensiv ---

**Grundprinzipien für die U13**

Schwerpunkt liegt auf fairem Spiel und Respekt beim Körperspiel. Körperkontakt bei U13 ist auf „Schieben in gleicher Laufrichtung" beschränkt und muss systematisch geübt werden.

**Schlittschuhtechniken**

Spitzenspieler müssen sich auf dem Eis perfekt bewegen können. Rückwärtslaufen, Übergänge von Vorwärts- zu Rückwärtsbewegung und richtiges Timing beim Check sind entscheidend.

**Schlägerarbeit**

„Stick on Puck" nimmt dem Gegner Optionen, indem Pass- und Schussbahnen blockiert werden. Techniken umfassen Poke-Checks und verschiedene Stockvarianten wie Lift-Check und Press-Check.

**Positionierung**

Verteidiger müssen kleinen Abstand halten, parallel zum Gegner bleiben und die Innenseite kontrollieren — zur Mitte bewegen, wenn der Gegner es tut.

**Trainingsmethoden**

Kleingruppenunterricht, Stationsübungen und Kleinfeldspiele entwickeln diese Techniken in realistischen Situationen.

