Klubs, die sich am DEL/DEB Technik-Test beteiligen und deren 15 beste Spieler zusammen einen Schnitt von mindestens 2,5 erreichen, erhalten Punkte für das Fünf-Sterne-Programm. Die Tests bewerten technische Ausführung, nicht Zeit.

