Helme und Handschuhe häufig vom Verein gestellt. Einstellmöglichkeiten werden wichtiger. Gute Ausrüstung ersetzt athletische und mentale Vorbereitung auf Körperkontakt niemals.

**Torwartausrüstung**

Wettkampfregularien beachten. Schlittschuhe und Schläger individuell anpassen. Torwartschienen beeinflussen häufig Spielstile — moderne Ausrüstung verwenden.

**Weiteres Equipment**

Stets verfügbar im Eisstadion: Duschsachen, Wechselwäsche, Schlägertape, Ersatzschuhbänder, Schere, Stutzentape, Faszienrolle/-ball, Tennis-/Lacrosse-Ball, Sportkleidung, persönliche Medikamente, Erste-Hilfe-Set, Riegel oder Sportgetränke, Supplemente.

