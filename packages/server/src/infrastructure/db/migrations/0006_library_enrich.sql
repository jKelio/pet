### --- Zweikampf offensiv ---

**Grundtechniken der Puckführung erweitern und verfeinern**

Spieler sollen lernen, den Puck sicher zu führen und dabei „den Kopf oben" zu halten, um das Spiel zu lesen. Unter Gegnerdruck müssen sie den Puck abschirmen können. Täuschungsmanöver zum Überwinden von Abwehrspielern in Eins-gegen-Eins-Situationen werden erlernt und verfeinert.

**Basis-Täuschungen**

Entscheidend für den offensiven Zweikampf:
- Front Fake einfach
- Front Fake zweifach
- Side Fake
- Gretzky Move
- Pull in Move
- Pull in Move mit Konterbewegung

**Den Puck abschirmen**

Zum Download stehen Stationentrainings und Spielformen zur Verfügung.

