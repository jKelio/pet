**Langfristig Leistung entwickeln!**

Das Nachwuchstraining ist in fünf Trainings- bzw. Ausbildungsstufen strukturiert: Basisausbildung, Grundlagentraining, Aufbautraining 1 und 2 sowie Anschlusstraining. Der Fokus liegt auf der ganzheitlichen Entwicklung der Spielerpersönlichkeit.

Training und Wettspiel stehen im Fokus des Trainers, wobei der langfristige Leistungsaufbau von Kindern und Jugendlichen Vorrang vor kurzfristigen Mannschaftserfolgen hat. Sportvereine müssen sich auf verändernde gesellschaftliche Rahmenbedingungen einstellen.

Elementare Bewegungsfertigkeiten, konditionelle und koordinative Fähigkeiten bilden die Basis für technische und taktische Ausbildung.

**Prinzipien des Nachwuchskonzeptes:**

1. Sportartenübergreifende Ausbildung zur Entwicklung koordinativer und konditioneller Fähigkeiten
2. Systematische, langfristige Leistungsentwicklung
3. Individuelle Betreuung, Förderung und Forderung
4. Gesamte Persönlichkeitsentwicklung im Vordergrund — der Mensch im Mittelpunkt
5. Betreuung durch verantwortungsvolle Jugendtrainer

Trainer und Betreuer müssen sich der besonderen Anforderungen bewusst sein. Soziale Kompetenz ist genauso wichtig wie gut organisiertes, motivierendes Training. Eine Bezugsperson zu sein und Vorbildfunktion wahrzunehmen sind zentrale Voraussetzungen für Nachwuchstrainer.

