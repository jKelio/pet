### --- Materialkunde ---

**Mit Freude ins Eishockey**

Richtige Ausrüstung ist für erfolgreiche Eishockey-Anfänger entscheidend. Unzureichendes Material in der Basisschulung dämpft nicht nur die Begeisterung, sondern behindert auch die Technikentwicklung.

**Schlittschuhe (U9/U11)**
- Nur Eishockey-Schlittschuhe von renommierten Herstellern über Fachhandel
- Richtige Passform ist entscheidend; keine zu großen Schlittschuhe
- Schuhe müssen fest sitzen ohne zu drücken oder zu locker zu sein
- Keine Hochleistungs-Karbonmodelle

**Schläger**
- Länge bis zum Mund (mindestens Kinn, maximal Nase)
- Flex 25–35 empfohlen, maximal 50; Flexibilität sollte das Körpergewicht in kg nicht überschreiten
- Youth- und Junior-Schläger haben altersgerechte Schaufelbreiten
- Keine gekürzten Erwachsenenschläger

**Helm und Gesichtsschutz**
- Helm mit Schutzgitter und/oder Plexiglas
- Helm muss ohne Druckpunkte fest sitzen, Kinnschutz muss am Kinn anliegen

**Schutzausrüstung**
- Handschuhe, Tiefschutz, Halskrause, Brustschutz, Ellenbogen- und Knieschoner, gepolsterte Hose, Schulter-/Brustschutz müssen eishockeyspezifisch und gut passend sein

**Torwartausrüstung**
- Schienen bis zur Oberschenkelmitte
- Fang- und Stockhandschuh müssen zur Handgröße passen
- Brustschutz darf Bewegung nicht übermäßig einschränken

