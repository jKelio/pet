Aggressiver Forecheck: F1 greift Puckführenden aggressiv an. F2 schließt Bande auf der starken Seite. F3 orientiert sich am zweiten Verteidiger. SsD (Starke-Seite-Verteidiger) unterstützt hochmittig an der blauen Linie. WsD bereit zu pinchen.

