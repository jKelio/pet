### --- Trainingssteuerung und Methodik ---

**Training muss wirksam sein**

Jede konkrete Bewegungssituation besteht aus einer Mischung von koordinativen und konditionell-energetischen Faktoren. Das betrifft sowohl athletisch-motorische sowie technische oder taktische Trainingsformen.

Die athletischen und motorischen Voraussetzungen haben dabei im langfristigen Leistungsaufbau nicht nur die Funktion eines Fundaments, auf dem — einmal gelegt — die Eishockey-Entwicklungsschritte aufbauen, sondern im Verlauf auch flankierende, stützende Aufgaben, ohne die eine spezifische Entwicklung sehr störanfällig wäre.

Um diese Funktionen und das Zusammenspiel der einzelnen Trainingsinhalte über die Ausbildungsstufen zu gewährleisten, gibt die DEB-Rahmentrainingskonzeption Hinweise zu den Umfängen der Trainingsschwerpunkte in den einzelnen Altersstufen. Diese sind als Anhaltspunkte zu verstehen und nicht als strikte Vorgaben, die 1 zu 1 umzusetzen sind.

