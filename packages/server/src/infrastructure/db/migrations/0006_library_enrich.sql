### --- Neutrale Zone ---

**Richtiges Verhalten in der neutralen Zone**

Pädagogische Ansätze haben Vorrang vor taktischen Anweisungen. Grundlegende taktische Prinzipien werden durch fundamentale Konzepte vermittelt.

**Zweikämpfe**

Gute Schlägerarbeit ist erforderlich (stick to puck). Mit Puckbesitz ist Puckschutz durch Körperpositionierung essenziell.

**Angriff (Rush)**

Während Übergängen sollten alle drei Bahnen besetzt sein, um Pässe von der Mitte zu den Außen zu üben. Zug zum Tor und Schaffung von Torchancen stehen im Fokus.

**Allgemeiner Forecheck**

Außenstürmer positionieren sich tiefer; einer attackiert den Puckträger auf der starken Seite, der dritte Stürmer bleibt höher für flexible Reaktion.

**Backcheck**

Bei Puckverlust in der Offensivzone hilft S3 den Verteidigern, während S1 und S2 schnell durch die Mitte zurück in das eigene Drittel laufen und bremsen.

**Bully**

Spieler lernen Positionierungen in allen Zonen und verstehen Laufwege nach Gewinn und Verlust.

---

### --- Offensive Zone ---

**Verhalten in der offensiven Zone in der U13-Altersklasse**

Grundlegendes taktisches Prinzip: „In der offensiven Zone sollte immer ein Spieler vor dem gegnerischen Tor sein, um angespielt werden zu können oder Nachschüsse zu verwerten."

**Gegenangriff (Rush)**

Alle drei Bahnen besetzt halten. „Kick out"-Pass von Center zu Außenpositionen. Zug zum Tor (Mittelbahn) und Schaffung von Torchancen.

**Allgemeiner Forecheck**

Die zwei Außenstürmer positionieren sich etwas tiefer. Der Stürmer auf der starken Seite initiiert Druck auf den Puckträger, der dritte Stürmer bleibt höher.

**Backcheck**

S3 unterstützt Verteidiger, S1 und S2 laufen schnell durch die Mitte zurück. Schulterblick für Passabfangen.

**Bully**

Positionierung für Bullys in allen Zonen sowie entsprechende Laufwege nach Gewinn und Verlust.

