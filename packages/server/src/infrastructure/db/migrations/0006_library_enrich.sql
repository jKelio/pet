Jede erfolgreiche taktische Struktur basiert auf dem Gewinnen von Zweikämpfen im 1-gegen-1. Realisierbar ist ein taktisches Konzept für die Defensive nur auf dem Boden einer entsprechenden technischen Grundlage. In der U20-Altersklasse darf nicht der Fehler gemacht werden, nur die mannschaftstaktischen Elemente der Defensive zu trainieren.

