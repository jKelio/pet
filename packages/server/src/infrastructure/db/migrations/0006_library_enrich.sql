### --- Stocktechnik ---

**Systematischen Aufbau eines Stocktechnik-Inventars**

Spieler sollen „ein immer besseres Gefühl für den Schläger und das Spielgerät, den Puck" entwickeln.

**Schlägereigenschaften und Griff**

Altersgerechte Schläger sind essentiell. Die optimale Länge reicht bis zur Mundhöhe. Der Griff sollte etwa Schulterbreite betragen, wobei die obere Hand fest am Ende und die untere Hand beweglich am Schaft positioniert ist.

**Puckführung und Finten**

Die sichere Puckführung bildet die Grundlage für Täuschungen. Wichtige Elemente sind Timing, Täuschungsbewegung, Richtungsänderung, Beschleunigung und Puckkontrolle. Basisbewegungen umfassen kurzes und weites Dribbeln in verschiedenen Ebenen sowie Ziehbewegungen.

**Passen**

Der „Push-Pull"-Pass wird betont als zentrale Technik. Spieler sollen Pässe in Bewegung präzise abgeben und annehmen. Trainingsformen wie Passpendel, Passfünfeck und Timing-Übungen sind empfohlen.

**Schießen**

Verschiedene Schusstechniken: Handgelenkschuss, Zweikontaktschuss, Flexschuss und Schlagschuss. Die Trainingsorganisation betont hohe Wiederholungszahlen durch Frontal- oder Stationstraining.

Die gesamte Ausbildung folgt dem „STS-Methode"-Konzept (Spielen-Trainieren-Spielen) mit regelmäßigen Kleinfeldpielen.

---

### --- Schlittschuhtechnik ---

**„Ohne Schlittschuhlaufen — kein Eishockeyspielen"**

Das Programm konzentriert sich darauf, das im Anfängerkurs erworbene technische Repertoire schrittweise auszubauen. Die Puck-Integration in Übungen wird als wichtiger Trainingsbestandteil dieser Altersgruppe hervorgehoben.

Schlittschuhlauf-Bewegungen sind nicht Teil des natürlichen Bewegungsrepertoires eines Menschen und müssen daher über längere Zeit erlernt, wiederholt und gefestigt werden.

Die Trainingsfortschrittsgeschwindigkeit hängt von lokalen Faktoren ab — Eiszeit, wöchentliche Trainingsfrequenz und Mannschaftshomogenität — wobei der Trainer die Progression letztendlich bestimmt.

Die Freude am Eishockeysport bleibt oberste Priorität, gestützt durch abwechslungsreiches, gut strukturiertes Training, das an die spezifischen Mannschaftsbedürfnisse angepasst ist.

