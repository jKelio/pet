### --- Schlittschuhtechnik ---

**Grundlagen schaffen — Technik forcieren**

„Ohne Schlittschuhlaufen — kein Eishockeyspielen: Die Basis für jeden Eishockeyspieler ist das Schlittschuhlaufen."

Das Kernziel im Grundtraining ist die Entwicklung eines grundlegenden Gefühls für das Schlittschuhlaufen und das Eis selbst. Schlittschuhlauf-Bewegungen sind nicht Teil des natürlichen Bewegungsrepertoires eines Menschen und erfordern deshalb längere Übungsphasen.

**Trainingsphilosophie**

Die Freude am Sport hat in dieser Entwicklungsphase Priorität. Die „STS-Methode" (Spielen-Trainieren-Spielen) hält den Fokus auf dem Spiel selbst. Kleinfeldspiele bleiben das bevorzugte Lehrwerkzeug.

**Trainingsstruktur**

Zwei Ansätze existieren: isolierte eishockeyspezifische Schlittschulung oder kombiniertes progressives Gruppentraining. Der Trainer trägt die Verantwortung: „Die Aufgabe des Trainers ist es, den Kindern eine ausgezeichnete Erfahrung zu bieten, damit sie wiederkommen wollen."

Die Ausbildung betont altersgerechte Kommunikation. Trainer sollten auf Augenhöhe der Kinder gehen, anstatt längere theoretische Erklärungen zu liefern.

Das U7/U9-Training folgt einer dreiteiligen Struktur: Spielen — Techniken erleben — Spielen.

