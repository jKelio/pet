### --- Grundkenntnisse der Spielregeln ---

Die Spieloffiziellen sorgen für sichere und faire Spielbedingungen. Sie klassifizieren Standardstrafen in drei Kategorien: Strafen, die Torchancen verhindern; solche mit hohem Verletzungsrisiko; und offensichtliche Strafen. Bei anderen Fouls haben Schiedsrichter Ermessensspielraum, was besonders bei Judgment-Strafen zu Diskussionen führt.

**Grundkenntnisse U9/U11**

Wichtige Strafen: Halten, Haken, Behinderung und Beinstellen. Bei Stockschlägen sind unnötige oder rücksichtslose Schläge auf Körper oder Hände strafbar. Stock-gegen-Stock-Aktionen unterhalb des unteren Handschuhes sind erlaubt. Bei Toren mit dem Schlittschuh führt nur eine deutliche Kickbewegung zur Annullierung; das bloße Lenken des Pucks ist zulässig.

**Grundkenntnisse U13**

Trainer sollten sich bewusst sein, dass Schiedsrichter nicht perfekt sind. Der Torschuss mit zu hohem Stock führt zur Torannullierung. Ein respektvoller Umgang mit Spieloffiziellen ist beruflich unerlässlich, und Trainer haben eine Vorbildfunktion.

**Grundkenntnisse U15/U17/U20**

Gefährliche Aktionen umfassen Boarding, Checks von hinten, gegen den Kopf und gegen das Knie. Bewertungskriterien berücksichtigen Puckbesitz, Kraft, Körperhaltung und Absicht. Trainer sollten verstehen, warum Spieler Fouls begehen — oft aufgrund schlechter defensiver Position, Erschöpfung oder mangelnder Kontrolle.

