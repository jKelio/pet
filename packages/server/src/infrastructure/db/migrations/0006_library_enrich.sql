Trap: F1 teilt das Eis und verhindert Querpässe. F2 und F3 verteidigen die offensive Blaue Linie/Raum. SsD auf der Mittellinie, eigene blaue Linie verteidigen. WsD sofort zu Mitte/Abwehrseite.

**Zweikampfverhalten beim Backchecking**

Ziel: Gegner unter Druck setzen, zu Scheibenverlusten zwingen oder Puck abnehmen.

Elementare Prinzipien: Schnell Schlittschuhlaufen; Mitte verteidigen; 5 Schläger auf dem Eis; zurück ins eigene Haus.

Pushback (Körperangriff zum Scheibengewinn): bei gleicher Höhe; Entscheidung an der Roten Linie; Schläger voran.

**Zweikampfverhalten des Verteidigers 1-1 durch die neutrale Zone**

Wichtigste Aufgabe: Lücke (Gap) zum Angreifer klein halten (Schlägerlänge). Mittelbahn verteidigen und Gegner nach außen drängen. Wenn möglich: Angreifer an der eigenen blauen Linie stellen. Immer mit langem Schläger Distanz halten; Außenschulter auf Höhe der Außenschulter des Stürmers.

